#include "libft.h"

int	ft_isalnum(int ch)
{
	if ('A' <= ch && ch <= 'Z')
		return (1);
	else if ('a' <= ch && ch <= 'z')
		return (1);
	else if (48 <= ch && ch <= 57)
		return (1);
	else
		return (0);
}
